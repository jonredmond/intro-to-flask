# Integers
x = 10
print(x)
y = x * 2
print(y)

# Floats
x = 3.14159
print(x)
y = x ** 2
print(y)

# Complex
# j is used to denote the imaginary part of a complex number (following engineerings standards rather than mathematical)
x = 1+2j
print(x)
y = (1+2j)*(3+4j)
# (1+2j)(3+4j)
# (3+6j+4j+8j^2)
# (3+10j+-8)
# (-5+10j)
print(y)
